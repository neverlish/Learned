package ex_round_functions

import kotlin.math.*

fun main(args: Array<String>) {
    print("${ceil(3.2)} ")
    print("${ceil(3.7)} ")
    print("${floor(3.2)} ")
    print("${floor(3.7)} ")
    print("${round(3.2)} ")
    print("${round(3.7)} ")
    print("${15.83.roundToInt()} ")
    print("${37842.45.roundToLong()} ")
}