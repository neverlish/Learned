package ex_trigonometric_function

import kotlin.math.*

fun main(args: Array<String>) {
    println(sin(PI / 4))
    println(cos(PI / 4))
    println(tan(PI / 4))

    println(asin(PI / 4))
    println(acos(PI / 4))
    println(atan(PI / 4))

    println(sinh(PI / 4))
    println(cosh(PI / 4))
    println(tanh(PI / 4))

    println(asinh(PI / 4))
    println(acosh(PI / 4))
    println(atanh(PI / 4))
}